<h3>1. Definiciones</h3>

<p>En el presente contrato serán de aplicación las definiciones que se detallan más abajo. Asimismo, las palabras en singular incluirán el plural y viceversa.</p>

<ul>
	<li><strong>“Atención Inmediata”</strong>: programa informático titularidad de la Compañía, que permite al Cliente, entre otras funcionalidades, atenderse de forma inmediata en nuestra plataforma, con aquellos Especialistas que estén disponibles.</li>
	<li><strong>“Cliente”</strong>: Persona física o jurídica, según el caso, que suscribe este Contrato con Emeeds para beneficiarse del Servicio Premium.</li>
	<li><strong>“Condiciones Particulares”</strong>: las condiciones particulares para el otorgamiento del Servicio Premium ejecutadas entre el Cliente y Emeeds en conexión con este Contrato.</li>
	<li><strong>“Contrato”</strong>: El presente contrato de prestación de servicios incluyendo, las presentes Condiciones Generales, las Condiciones Particulares y todas sus cláusulas y/o Anexos.</li>
	<li><strong>“Cuenta”</strong>: Entrada en la base de datos del Sitio Web que confiere la condición de Cliente con acceso a los Servicios Premium, una vez introducido el usuario y la contraseña correspondientes.</li>
</ul>

<h3>2. Definiciones Externas.</h3>

<p>En el presente contrato serán de aplicación las definiciones&nbsp; externas que se detallan más abajo. Asimismo, las palabras en singular incluirán el plural y viceversa.</p>

<ul>
	<li>“Partes”: La Compañía y el Cliente de forma conjunta.</li>
	<li>“Perfil”: Información del Profesional publicada en una subpágina del Sitio Web identificada con una dirección URL diferente a la del Sitio Web pero perteneciente a la Compañía.</li>
	<li><strong>“Período de Facturación”</strong>: Cada uno de los meses o años naturales, en función del período inicial y tipo de suscripción, que se iniciarán a computar a partir de la fecha de asignación del primer Perfil a la Cuenta, en los términos previstos en las presente Condiciones Generales.</li>
	<li><strong>“Profesional”</strong>: Profesional cuya actividad consiste en la prestación de servicios médicos o paramédicos, entre los que se encuentran, entre otros, facultativos, médicos, dentistas, psicólogos, fisioterapeutas, nutricionistas, terapeutas, psicólogos, etc. En el supuesto en el que el Cliente sea una persona física, el Profesional y el Cliente serán la misma persona y, por consiguiente, las actividades anteriores serán prestadas directamente por el Cliente.</li>
	<li><strong>“Sitio Web”</strong>: Herramienta en línea disponible en la siguiente dirección https://www.emeeds.cl y operada por Emeeds que permite, entre otros, que los Profesionales puedan ofrecer sus servicios, así como la puesta en contacto de Profesionales con pacientes que buscan especialistas.</li>
	<li><strong>“Usuario”</strong>: Persona física que tiene una cuenta o accede al Sitio Web.​</li>
</ul>

<h3>3. Asignación de perfiles</h3>

<p>3.1 Una vez suscrito el presente Contrato, el Cliente deberá asignar Perfiles a su Cuenta a los efectos de poder disfrutar de los Servicio Premium. El Perfil deberá ser asignado de forma individual por parte del Cliente o por un empleado de Emeeds previa solicitud y aportación de la documentación/información necesaria por parte del Cliente. En todo caso, la asignación del Perfil a la Cuenta del Cliente se registrará en el sistema de Emeeds.</p>

<p>3.2 Sin perjuicio de lo dispuesto en el apartado anterior, mediante la firma del presente Contrato, el Cliente acepta, consiente y autoriza de forma expresa a Emeeds para que pueda asignar a su Cuenta el número de Perfiles acordados en las Condiciones Particulares, en el supuesto en el que el Cliente no hubiese realizado dicha asignación en el plazo máximo de 14 días a contar desde la fecha de suscripción del Contrato.</p>

<p>3.3 El Cliente únicamente podrá asignar o solicitar la asignación de Perfiles a Profesionales que presten servicios de forma presencial en la dirección o centro de trabajo del Cliente o cualquier otra dirección en la que el Cliente realice una actividad empresarial relacionada con la salud y pueda ser probado todo ello de forma suficiente a la libre discreción de Emeeds. En el supuesto en el que el Cliente sea una persona física, el Profesional asignado deberá ser el propio Cliente.</p>

<p>3.4 El Cliente autoriza y permite a Emeeds modificar el contenido, materiales y documentos facilitados por parte del Cliente a los efectos de preparar el Perfil. En este sentido, cualquier modificación realizada se limitará a actuaciones encaminadas a mejorar la calidad de la publicación y validar la veracidad de la misma (eliminar erratas, mejorar la calidad de las imágenes, confirmar la veracidad de la especialidad del Profesional con, entre otros, el colegio de médicos correspondiente, etc.).</p>

<p>3.5 El Cliente autoriza y acepta que, en el caso de que un Profesional trabaje también para otros clientes de Emeeds, su Perfil podrá estar asignado a otros clientes de Emeeds también, sin que ello implique en ningún caso un incumplimiento del presente Contrato.</p>

<h3>4. Aumento y disminución de los perfiles asignados</h3>

<p>4.1 Aumento de Perfiles: el Cliente podrá incrementar el número de Perfiles asignados a su Cuenta en cualquier momento, de manera ilimitada y sin necesidad de modificar el presente Contrato. En caso de producirse un aumento en el número de Perfiles, el Cliente acepta y entiende que la Contraprestación correspondiente será aumentada de acuerdo con lo establecido en la cláusula 5 del presente acuerdo.</p>

<p>4.2 Disminución de perfiles: el Cliente tendrá derecho a reducir el número de Perfiles asignados a su Cuenta de acuerdo con el detalle que se muestra a continuación:</p>

<p style="margin-left: 10px;">a. Si el Cliente ha contratado entre 1 y 9 perfiles, no podrá disminuir los Perfiles asignados a su Cuenta.</p>

<p style="margin-left: 10px;">b. Si el Cliente ha contratado entre 10 y 40 Perfiles, podrá disminuir en un máximo de 2 Perfiles los Perfiles asignados.</p>

<p style="margin-left: 10px;">c. Si el Cliente ha contratado entre 41 y 60 Perfiles, podrá disminuir en un máximo de 3 Perfiles los Perfiles asignados.</p>

